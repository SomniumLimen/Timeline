const imageBase = "./assets/docx-images/";
const nodesEl = document.querySelector("#timelineNodes");
const panelEl = document.querySelector("#storyPanel");
const mediaEl = document.querySelector("#storyMedia");
const timeEl = document.querySelector("#storyTime");
const titleEl = document.querySelector("#storyTitle");
const subtitleEl = document.querySelector("#storySubtitle");
const toneEl = document.querySelector("#storyTone");
const textEl = document.querySelector("#storyText");
const clockTimeEl = document.querySelector("#clockTime");
const clockHandEl = document.querySelector("#clockHand");

let activeIndex = 0;

function timeToClockAngle(time) {
  const [hours, minutes] = time.split(":").map(Number);
  return ((hours % 12) + minutes / 60) * 30;
}

function renderNodes() {
  nodesEl.innerHTML = stories
    .map(
      (story, index) => `
        <button class="node${index === activeIndex ? " active" : ""}" type="button" data-index="${index}" aria-pressed="${index === activeIndex}">
          <span class="node-time">${story.time}</span>
          <span class="node-card">
            <strong>${story.title}</strong>
            <span>${story.entry}</span>
          </span>
        </button>
      `
    )
    .join("");
}

function renderStory(index) {
  const story = stories[index];
  activeIndex = index;

  mediaEl.innerHTML = story.images
    .map(
      (image, imageIndex) => `
        <figure>
          <img src="${imageBase}${image}" alt="${story.title}相关图片${imageIndex + 1}" />
        </figure>
      `
    )
    .join("");

  timeEl.textContent = `${story.time} / ${story.entry}`;
  clockTimeEl.textContent = story.time;
  clockHandEl.style.setProperty("--clock-angle", `${timeToClockAngle(story.time)}deg`);
  titleEl.textContent = story.title;
  subtitleEl.textContent = story.subtitle;
  toneEl.textContent = story.tone;
  textEl.innerHTML = story.paragraphs
    .map((paragraph) => {
      const className = paragraph.startsWith("「") ? ' class="quote"' : "";
      return `<p${className}>${paragraph}</p>`;
    })
    .join("");

  renderNodes();
  panelEl.classList.remove("entering");
  requestAnimationFrame(() => {
    panelEl.classList.add("entering");
  });
}

nodesEl.addEventListener("click", (event) => {
  const button = event.target.closest(".node");
  if (!button) {
    return;
  }
  renderStory(Number(button.dataset.index));
});

renderNodes();
renderStory(activeIndex);
